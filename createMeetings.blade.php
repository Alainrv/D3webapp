<script>

	$(document).ready(function(){
		$(".historyBox").removeClass('hideBox');
		$(".upcomingBox").removeClass('hideBox');
		$(".upcomingBox").hide();
	    $(".historyBox").hide();
	});

	// function to change the booking tabs and booking displays 
	// when click on tabs
	$(".tab").click(function(){
		// remove onSelect class from another tab
		$('.onSelect').removeClass("onSelect");
		// add onSelect class to the clicked tab
    	$(this).addClass('onSelect');
    	// change the bottom border position
    	// assign bot border to a variable botBor
    	var $botBor = $(this).closest('.meetingTabs').find('.selectBorder');

    	// assign each meeting box to a variable
    	var $pending = $('.pendingBox');
    	var $upcoming = $('.upcomingBox');
    	var $history = $('.historyBox');
    	
    	// click pending, move the botBor to left
    	// and show upcoming box
    	// click upcoming, move botBor to middle 
    	// and show upcoming box
    	// click history, move botBor to right
    	// and show history box
    	if($(this).hasClass("pending")){
    		$botBor.css("left",'0%');
    		$history.hide();
    		$upcoming.hide();
    		$pending.show();
    		$pending.css('display','');

    	} else if($(this).hasClass("upcoming")) {
    		$botBor.css("left", "calc(100% / 3)");
    		$history.hide();
    		$pending.hide();
    		$upcoming.show();
    		$upcoming.css('display','');

    	} else{
    		$botBor.css("left", "calc(100% / 3 * 2)");
    		$pending.hide();
    		$upcoming.hide();
    		$history.show();
    		$history.css('display','');
    	}
	});

	$(".toggleSign").click(function (){
		// assign booking content to a variable
		$content = $(this).closest(".bookingInfor").find('.toggleContent');
		// if content is hiddent, show up arrow. If not show down arrwo
	    if($content.css("display")=="none"){
			$(this).css("transform","rotateX(180deg)");	   
		} 
	    else{
    		$(this).css("transform","rotateX(0deg)");
	    }
	    // toggle the content on click
	    $content.slideToggle(500)
	});
	// function used to count the pending meetings
	// and show the number as the notification
	var countPending = function(){
		var $pendingCount = $('.pendingBox .roomBooking').length;
		if ($pendingCount == 0){
			$("#pendingCount").css("display","none");
		} else {
			$("#pendingCount").css("display","block");
			$('#pendingCount').html($pendingCount);
		}
	}
	countPending();
	
	// cancel the meeting when click cancel,
	// when it is pending meeting, update the pending meeting numbers
	$('.cancel').click(function(){
		$item = $(this).closest('.roomBooking');
		$item.slideToggle(500);

		setTimeout(function() {
            $item.remove();
            countPending();
        }, 600);

	});

</script>

